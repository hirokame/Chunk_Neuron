function GaitAnalysis3(data)
%フレーム解析　データはsize(num_frame,4)の行列　右前、左前、右後、左後
data=GetData;%dataをコマンドで与える場合にはコメントアウト
% data(1:3,:)

RH=data(:,3);
RF=data(:,1);
LF=data(:,2);
RLF3=data(:,1:3);

% Lrh=length(RH)
% StartRHを基準にした各Cycle（1トライアル）における、swing,stanceの割合を求める。
% 前足と後足について

StartRH=[];
EndRH=[];CycleRHArray=[];
for n=2:length(RH)
    if RH(n)==0 && RH(n-1)==1
        StartRH=[StartRH n];
        if length(StartRH)>1
            CycleRH=StartRH(end)-StartRH(end-1);
            CycleRHArray=[CycleRHArray CycleRH];
        end
    end
end

% CycleRHArray

RatioFore11=[];
RatioFore01=[];
RatioFore10=[];
RatioFore00=[];

RatioHind11=[];
RatioHind01=[];
RatioHind10=[];
RatioHind00=[];

for n=1:length(StartRH)-1
    
    Fore11=0;Fore01=0;Fore10=0;Fore00=0;
    Hind11=0;Hind01=0;Hind10=0;Hind00=0;

%     for p=StartRH(n):StartRH(n+1)-1
    for p=1:length(data(:,1))
        if p>StartRH(n) & p<=StartRH(n+1)
            if data(p,1)==1 && data(p,2)==1
                Fore11=Fore11+1;
            elseif data(p,1)==0 && data(p,2)==1
                Fore01=Fore01+1;
            elseif data(p,1)==1 && data(p,2)==0
                Fore10=Fore10+1;
            elseif data(p,1)==0 && data(p,2)==0
                Fore00=Fore00+1;
            end
            
            if data(p,3)==1 && data(p,4)==1
                Hind11=Hind11+1;
            elseif data(p,3)==0 && data(p,4)==1
                Hind01=Hind01+1;
            elseif data(p,3)==1 && data(p,4)==0
                Hind10=Hind10+1;
            elseif data(p,3)==0 && data(p,4)==0
                Hind00=Hind00+1;
            end
            
        end
    end
    
    RatioFore11=[RatioFore11 Fore11/(StartRH(n+1)-StartRH(n))];
    RatioFore01=[RatioFore01 Fore01/(StartRH(n+1)-StartRH(n))];
    RatioFore10=[RatioFore10 Fore10/(StartRH(n+1)-StartRH(n))];
    RatioFore00=[RatioFore00 Fore00/(StartRH(n+1)-StartRH(n))];
    
    RatioHind11=[RatioHind11 Hind11/(StartRH(n+1)-StartRH(n))];
    RatioHind01=[RatioHind01 Hind01/(StartRH(n+1)-StartRH(n))];
    RatioHind10=[RatioHind10 Hind10/(StartRH(n+1)-StartRH(n))];
    RatioHind00=[RatioHind00 Hind00/(StartRH(n+1)-StartRH(n))];
    
end
MeanRatioFore11=mean(RatioFore11)
MeanRatioFore01=mean(RatioFore01)
MeanRatioFore10=mean(RatioFore10)
MeanRatioFore00=mean(RatioFore00)
StdRatioFore11=std(RatioFore11)
StdRatioFore01=std(RatioFore01)
StdRatioFore10=std(RatioFore10)
StdRatioFore00=std(RatioFore00)

MeanRatioHind11=mean(RatioHind11)
MeanRatioHind01=mean(RatioHind01)
MeanRatioHind10=mean(RatioHind10)
MeanRatioHind00=mean(RatioHind00)
StdRatioHind11=std(RatioHind11)
StdRatioHind01=std(RatioHind01)
StdRatioHind10=std(RatioHind10)
StdRatioHind00=std(RatioHind00)
    
LRRatioFore=[MeanRatioFore11 MeanRatioFore01 MeanRatioFore10 MeanRatioFore00]
LRRatioForeStd=[StdRatioFore11 StdRatioFore01 StdRatioFore10 StdRatioFore00]

LRRatioHind=[MeanRatioHind11 MeanRatioHind01 MeanRatioHind10 MeanRatioHind00]
LRRatioHindStd=[StdRatioHind11 StdRatioHind01 StdRatioHind10 StdRatioHind00]

figure
bar(LRRatioFore);hold on
errorbar(LRRatioFore,LRRatioForeStd,'k+ ')

figure
bar(LRRatioHind);hold on
errorbar(LRRatioHind,LRRatioHindStd,'k+ ')


